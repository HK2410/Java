package Learnings;

public class PatternProgram19 {

	public static void main(String[] args)
	{
		
		for(int row = 5;row>=1;row--)
		{
		   for(int col = row;col>=1;col--)
		   {
			   System.out.print(col);
		   }
		   System.out.println();
		}
		
		for(int row = 2;row<=5;row++)
		{
		   for(int col =row;col>=1;col--)
		   {
			   System.out.print(col);
		   }
		   System.out.println();
		}

}}
