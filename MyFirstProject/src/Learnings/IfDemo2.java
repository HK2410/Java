package Learnings;

public class IfDemo2 {

	public static void main(String[] args) {
		String name = "Kumar";
		
		if(name == "Naveen")
		{
			System.out.println("Hello Naveen");
		}
		else if(name == "Surya")
		{
			System.out.println("Hello Surya");
		}
		else if(name =="Kumar")
		{
			System.out.println("Hello Kumar");
		}
		else
		{
			System.out.println("Hello Everybody");
		}
	}

}
