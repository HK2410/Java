public class IfDemo3 {

	public static void main(String[] args) {
		
		int i = 30;
		
		if(i == 10)
		{
			System.out.println("i is 10");
		}
		
		else if(i == 20)
		{
			System.out.println("i is 20");
		}
		
		else if(i == 30)
		{
			System.out.println("i is 30");
		}
		
		else
		{
			System.out.println("i is larger");
		}
	}

}
